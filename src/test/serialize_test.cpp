std::string asHex( const Bytes& bytes )
{
     static std::ostringstream oss;
     oss.str( {} );
     boost::algorithm::hex( bytes.cbegin(), bytes.cend(),
          std::ostreambuf_iterator< char >( oss ) );
     return oss.str();
}


